from __future__ import annotations

import argparse
import json
from pathlib import Path
from typing import Any


def format_seconds(value: float) -> str:
    if value < 1e-6:
        return f"{value * 1e9:.1f} ns"
    if value < 1e-3:
        return f"{value * 1e6:.1f} us"
    if value < 1:
        return f"{value * 1e3:.3f} ms"
    return f"{value:.3f} s"


def load_payload(path: Path) -> dict[str, Any]:
    return json.loads(path.read_text(encoding="utf-8"))


def render_markdown(payload: dict[str, Any], *, source_path: Path) -> str:
    results = payload["results"]
    measured = [result for result in results if not result.get("skip_reason")]
    skipped = [result for result in results if result.get("skip_reason")]
    metric_names = [metric["name"] for metric in measured[0]["metrics"]] if measured else []

    lines = [
        "# Latest Benchmark Results",
        "",
        "Generated from the saved JSON snapshot. Do not edit this file by hand.",
        "",
        f"Source JSON: `{source_path.as_posix()}`",
        "",
        f"Root: `{payload.get('root', '')}`",
        "",
        "Related docs:",
        "",
        "- `../README.md`",
        "- `../BENCHMARK_SUMMARY.md`",
        "- `../COMPETITIVE_COMPARISON.md`",
        "- `README.md`",
        "",
        "Values below are median latency per operation. Lower is better.",
        "",
        "| library | " + " | ".join(metric_names) + " |",
        "|" + "---|" * (len(metric_names) + 1),
    ]

    for result in measured:
        metric_map = {metric["name"]: metric for metric in result["metrics"]}
        row = [result["library"]]
        row.extend(format_seconds(metric_map[name]["median"]) for name in metric_names)
        lines.append("| " + " | ".join(row) + " |")

    if skipped:
        lines.extend(
            [
                "",
                "## Skipped Libraries",
                "",
            ]
        )
        for result in skipped:
            lines.append(f"- `{result['library']}`: {result['skip_reason']}")

    return "\n".join(lines) + "\n"


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Render benchmark JSON results as markdown.")
    parser.add_argument(
        "input_json",
        nargs="?",
        default="benchmarks/latest-results.json",
        help="Path to the benchmark JSON snapshot.",
    )
    return parser.parse_args()


def main() -> int:
    args = parse_args()
    path = Path(args.input_json)
    print(render_markdown(load_payload(path), source_path=path), end="")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
